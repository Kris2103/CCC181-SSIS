-- Create users table
CREATE TABLE IF NOT EXISTS users
(
    id SERIAL PRIMARY KEY,
    username VARCHAR(100) NOT NULL UNIQUE,
    email VARCHAR(150) NOT NULL UNIQUE,
    password VARCHAR(100) NOT NULL
);

-- Create colleges table
CREATE TABLE IF NOT EXISTS colleges
(
    college_code VARCHAR(10) PRIMARY KEY,
    college_name VARCHAR(100) NOT NULL
);

-- Create programs table with foreign key constraint
CREATE TABLE IF NOT EXISTS programs
(
    program_code VARCHAR(10) PRIMARY KEY,
    program_name VARCHAR(100) NOT NULL,
    college_code VARCHAR(10),
    CONSTRAINT fk_program_college_code FOREIGN KEY (college_code)
        REFERENCES colleges (college_code)
        ON UPDATE CASCADE
        ON DELETE SET NULL
);

-- Create students table with foreign key constraint
CREATE TABLE IF NOT EXISTS students
(
    id_number VARCHAR(12) PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    gender VARCHAR(10) NOT NULL CHECK (gender IN ('Male', 'Female')),
    year_level SMALLINT NOT NULL,
    program_code VARCHAR(10),
    profile_picture_url VARCHAR(255) DEFAULT 'default_avatar.png',
    CONSTRAINT fk_student_program_code FOREIGN KEY (program_code)
        REFERENCES programs (program_code)
        ON UPDATE CASCADE
        ON DELETE SET NULL
);

-- Create indexes for better query performance
CREATE INDEX IF NOT EXISTS idx_colleges_college_code ON colleges(college_code);
CREATE INDEX IF NOT EXISTS idx_programs_program_code ON programs(program_code);
CREATE INDEX IF NOT EXISTS idx_students_id ON students(id_number);





